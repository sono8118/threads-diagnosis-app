/**
 * タイプのメタデータ
 */
export interface TypeMetadata {
  /** タイプ名 */
  name: string;
  /** タイプ説明 */
  description: string;
  /** サブテキスト */
  subText: string;
  /** 次の一歩（3段階） */
  nextSteps: {
    /** まずできそうなこと（今日） */
    today: string;
    /** 少し慣れたら（今週） */
    thisWeek: string;
    /** 余裕が出てきたら（今月） */
    thisMonth: string;
  };
  /** CTA（Call to Action）文言 */
  cta: {
    /** タイトル（見出し） */
    title: string;
    /** 説明文（本文） */
    description: string;
    /** ボタンテキスト */
    buttonText: string;
  };
}

